def new_timer():
    return defaultdict(float)

def expit(x):
    return np.exp(x) / (1 + np.exp(x))


def recipient_donor_indices(g):
    rc = np.where(g == 1)[0]
    dn = np.where(g == 0)[0]
    return rc, dn


def distance_matrix(X, g, dist_type=2):
    """
    Equivalent target of distMatrix_calc(df, dist.type, grp):
    rows = recipients, columns = donors.
    """
    rc, dn = recipient_donor_indices(g)
    X_rc = X[rc]
    X_dn = X[dn]

    if dist_type == 1:
        return np.abs(X_rc[:, None, :] - X_dn[None, :, :]).sum(axis=2)

    if dist_type == 2:
        return np.sqrt(((X_rc[:, None, :] - X_dn[None, :, :]) ** 2).sum(axis=2))

    if dist_type == 3:
        cov = np.cov(X, rowvar=False)
        inv_cov = np.linalg.pinv(cov)
        diff = X_rc[:, None, :] - X_dn[None, :, :]
        return np.einsum("...k,kl,...l->...", diff, inv_cov, diff)

    raise ValueError("dist_type must be 1, 2, or 3")


def lm_slope_variance(Y, g):
    """
    OLS for Y ~ intercept + g.
    Returns coefficient on g and its usual OLS variance.
    """
    X_design = np.column_stack([np.ones(len(g)), g])
    beta_hat = np.linalg.lstsq(X_design, Y, rcond=None)[0]
    resid = Y - X_design @ beta_hat
    n, p = X_design.shape
    sigma2 = (resid @ resid) / (n - p)
    vcov = sigma2 * np.linalg.inv(X_design.T @ X_design)
    return beta_hat[1], vcov[1, 1]


def wls_slope_variance(Y, g, weights):
    """
    Weighted least squares for Y ~ intercept + g.
    Returns coefficient on g and usual WLS variance.
    """
    X_design = np.column_stack([np.ones(len(g)), g])
    W = weights
    XTWX = X_design.T @ (W[:, None] * X_design)
    XTWY = X_design.T @ (W * Y)
    beta_hat = np.linalg.solve(XTWX, XTWY)
    resid = Y - X_design @ beta_hat
    n, p = X_design.shape
    sigma2 = (W * resid**2).sum() / (n - p)
    vcov = sigma2 * np.linalg.inv(XTWX)
    return beta_hat[1], vcov[1, 1]


def cluster_se_for_slope(Y, g, cluster):
    """
    Approximate cluster-robust standard error for coefficient on g.
    R code stores coeftest(...)[\"g.oc\", 2], which is a standard error,
    despite variable names like varclus.cl.
    """
    X_design = np.column_stack([np.ones(len(g)), g])
    beta_hat = np.linalg.lstsq(X_design, Y, rcond=None)[0]
    resid = Y - X_design @ beta_hat
    XtX_inv = np.linalg.inv(X_design.T @ X_design)

    meat = np.zeros((2, 2))
    for c in np.unique(cluster):
        idx = cluster == c
        Xc = X_design[idx]
        uc = resid[idx]
        s = Xc.T @ uc
        meat += np.outer(s, s)

    vcov = XtX_inv @ meat @ XtX_inv
    return float(np.sqrt(max(vcov[1, 1], 0.0)))


def mi_total_variance(estimates, within_vars):
    estimates = np.asarray(estimates)
    within_vars = np.asarray(within_vars)
    m = len(estimates)
    est_bar = estimates.mean()
    u_bar = within_vars.mean()
    b = ((estimates - est_bar) ** 2).sum() / (m - 1)
    t = u_bar + (1 + 1 / m) * b
    return est_bar, u_bar, b, t


