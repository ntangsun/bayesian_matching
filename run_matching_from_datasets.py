def run_unconstrained_1to1(X, Y, g, n_mcmc, beta, rng, profile=False):
    timing = new_timer()

    # -----------------------------
    # Setup
    # -----------------------------

    t0 = time.perf_counter()
    rc, dn = recipient_donor_indices(g)
    n_rc = len(rc)
    n_dn = len(dn)

    dist = distance_matrix(X, g, 2)
    offset = dist.min(axis=1)
    dist_df = dist + offset[:, None]

    # Original R uses p but does not define it. Use p = beta.
    p = beta
    alpha = (1 / (dist_df ** p))
    alpha = alpha / alpha.sum(axis=1, keepdims=True)
    alpha_cdf = np.cumsum(alpha, axis=1)
    alpha_cdf[:, -1] = 1.0
    timing["setup"] += time.perf_counter() - t0

    # -----------------------------
    # Allocation
    # -----------------------------

    t0 = time.perf_counter()
    est = np.empty(n_mcmc)
    estdiff = np.empty(n_mcmc)
    var = np.empty(n_mcmc)
    varpaired = np.empty(n_mcmc)

    Y_rc = Y[rc]
    timing["allocation"] += time.perf_counter() - t0

    # -----------------------------
    # MCMC
    # -----------------------------

    for j in range(n_mcmc):

        # ===== Sampling =====

        t0 = time.perf_counter()
        # 1) baseline 
        # match_cols = np.array([rng.choice(n_dn, p=alpha[r]) for r in range(n_rc)])
        # Y_match = Y[dn[match_cols]]

        # 2) uniform distribution
        u = rng.random(n_rc)
        match_cols = (u[:, None] > alpha_cdf).sum(axis=1)
        Y_match = Y[dn[match_cols]]
        timing["mcmc_sampling"] += time.perf_counter() - t0

        # ===== Estimation =====

        t0 = time.perf_counter()
        Y_oc = np.concatenate([Y_rc, Y_match])
        g_oc = np.concatenate([np.ones(n_rc), np.zeros(n_rc)])

        est[j], var[j] = lm_slope_variance(Y_oc, g_oc)
        estdiff[j] = Y_rc.mean() - Y_match.mean()
        varpaired[j] = np.var(Y_rc - Y_match, ddof=1) / len(Y_rc)
        timing["mcmc_estimation"] += time.perf_counter() - t0
    
    # -----------------------------
    # Summary
    # -----------------------------

    t0 = time.perf_counter()
    est_bar, u_bar, b, t = mi_total_variance(est, var)
    _, u_paired, _, t_paired = mi_total_variance(est, varpaired)
    timing["summary"] += time.perf_counter() - t0

    result = {
        "est": est_bar,
        "est_med": float(np.median(est)),
        "wiVar": u_bar,
        "bwVar": b,
        "miVar": t,
        "wiVarpaired": u_paired,
        "miVarpaired": t_paired,
        "estdiff": float(estdiff.mean()),
    }

    if profile:
        sampling_time = timing["mcmc_sampling"]
        estimation_time = timing["mcmc_estimation"]
        mcmc_time = sampling_time + estimation_time
        method_time = sum(timing.values())
        n_draws = int(n_mcmc * n_rc)

        result.update({f"time_{k}": v for k, v in timing.items()})

        result["time_mcmc_total"] = mcmc_time
        result["time_method_total"] = method_time

        result["n_sampling_draws"] = n_draws
        result["time_per_sampling_draw"] = sampling_time / n_draws

        result["share_sampling_in_mcmc"] = sampling_time / mcmc_time
        result["share_sampling_in_method"] = sampling_time / method_time
        result["share_estimation_in_method"] = estimation_time / method_time
        result["share_mcmc_in_method"] = mcmc_time / method_time

    return result

def run_unconstrained_1tok(
    X, Y, g, n_mcmc, beta, rng, k=2, profile=False
):
    timing = new_timer()

    # -----------------------------
    # Setup
    # -----------------------------
    t0 = time.perf_counter()

    rc, dn = recipient_donor_indices(g)
    n_rc = len(rc)
    n_dn = len(dn)

    dist = distance_matrix(X, g, 2)
    offset = dist.min(axis=1)
    dist_df = dist + offset[:, None]

    p = beta
    alpha = (1 / (dist_df ** p))
    alpha = alpha / alpha.sum(axis=1, keepdims=True)

    if not 1 <= k < n_dn:
        raise ValueError("k must be at least 1 and smaller than the number of donors.")

    # Each row is a mutable donor ordering for one recipient.
    # donor_order[r, :k] are currently matched donors.
    # donor_order[r, k:] are currently unmatched donors.
    # A proposed update samples one position from each block and swaps them
    # in place when accepted, avoiding repeated construction of S0 and S1.
    donor_order = np.argsort(dist, axis=1)

    timing["setup"] += time.perf_counter() - t0

    # -----------------------------
    # Allocation
    # -----------------------------
    t0 = time.perf_counter()

    est = np.empty(n_mcmc)
    estdiff = np.empty(n_mcmc)
    var = np.empty(n_mcmc)
    varpaired = np.empty(n_mcmc)
    tr = np.zeros(n_mcmc, dtype=bool)

    Y_rc = Y[rc]
    Y_matches = np.empty((n_rc, k))
    g_oc = np.concatenate([np.ones(n_rc), np.zeros(k * n_rc)])
    match_weights = np.full(n_rc * k, 1 / k)

    timing["allocation"] += time.perf_counter() - t0

    # -----------------------------
    # MCMC
    # -----------------------------
    for j in range(n_mcmc):
        accepted_any = False

        # ===== Sampling =====
        t0 = time.perf_counter()

        for r in range(n_rc):
            # Pick one selected donor to remove and one unselected donor to add.
            # The sampled values are donor-column indices; the sampled positions
            # let us mutate donor_order directly if the proposal is accepted.
            remove_pos = rng.integers(k)
            add_pos = rng.integers(k, n_dn)
            x1 = donor_order[r, remove_pos]
            x0 = donor_order[r, add_pos]

            prob_x1 = alpha[r, x1]
            prob_x0 = alpha[r, x0]

            w0 = prob_x0 / (1 - prob_x0)
            w1 = prob_x1 / (1 - prob_x1)

            logratio = np.log(w0 / w1)
            logr = min(0.0, logratio)

            if np.log(rng.random()) < logr:
                donor_order[r, remove_pos] = x0
                donor_order[r, add_pos] = x1
                accepted_any = True

            Y_matches[r, :] = Y[dn[donor_order[r, :k]]]

        timing["mcmc_sampling"] += time.perf_counter() - t0

        tr[j] = accepted_any

        # ===== Estimation =====
        t0 = time.perf_counter()

        Y_match = Y_matches.ravel()
        Y_oc = np.concatenate([Y_rc, Y_match])

        est[j], var[j] = lm_slope_variance(Y_oc, g_oc)

        estdiff[j] = (
            Y_rc.mean()
            - np.average(
                Y_match,
                weights=match_weights
            )
        )

        pair_avg = Y_matches.mean(axis=1)
        varpaired[j] = (
            np.var(Y_rc - pair_avg, ddof=1)
            / len(Y_rc)
        )

        timing["mcmc_estimation"] += time.perf_counter() - t0

    # -----------------------------
    # Summary
    # -----------------------------
    t0 = time.perf_counter()

    est_bar, u_bar, b, t = mi_total_variance(est, var)
    _, u_paired, _, t_paired = mi_total_variance(est, varpaired)

    timing["summary"] += time.perf_counter() - t0

    result = {
        "est": est_bar,
        "est_med": float(np.median(est)),
        "k": k,
        "wiVar": u_bar,
        "bwVar": b,
        "miVar": t,
        "wiVarpaired": u_paired,
        "miVarpaired": t_paired,
        "estdiff": float(estdiff.mean()),
        "acceptance_count": int(tr.sum()),
    }

    if profile:
        sampling_time = timing["mcmc_sampling"]
        estimation_time = timing["mcmc_estimation"]
        mcmc_time = sampling_time + estimation_time
        method_time = sum(timing.values())

        result.update({f"time_{k}": v for k, v in timing.items()})

        result["time_mcmc_total"] = mcmc_time
        result["time_method_total"] = method_time

        # Here a "sampling draw" means one recipient update.
        result["n_sampling_draws"] = int(n_mcmc * n_rc)
        result["time_per_sampling_draw"] = (
            sampling_time / result["n_sampling_draws"]
        )

        result["share_sampling_in_mcmc"] = (
            sampling_time / mcmc_time
        )
        result["share_sampling_in_method"] = (
            sampling_time / method_time
        )
        result["share_estimation_in_method"] = (
            estimation_time / method_time
        )
        result["share_mcmc_in_method"] = (
            mcmc_time / method_time
        )

    return result


def run_unconstrained_1tok_mc(
    X, Y, g, n_mcmc, beta, rng, k=2, profile=False
):
    timing = new_timer()

    # -----------------------------
    # Setup
    # -----------------------------
    t0 = time.perf_counter()

    rc, dn = recipient_donor_indices(g)
    n_rc = len(rc)
    n_dn = len(dn)

    dist = distance_matrix(X, g, 2)
    offset = dist.min(axis=1)
    dist_df = dist + offset[:, None]

    p = beta
    alpha = (1 / (dist_df ** p))
    alpha = alpha / alpha.sum(axis=1, keepdims=True)

    if not 1 <= k < n_dn:
        raise ValueError("k must be at least 1 and smaller than the number of donors.")

    # Under alpha_prime = 1 - alpha, the collapsed conditional Bernoulli
    # row weights are r_ij = alpha_ij / (1 - alpha_ij).
    row_weights = alpha / (1 - alpha)

    # Build the dynamic-programming table once for this simulation replication.
    # dp[r, j, kk] is the total product weight over all subsets of size kk
    # available from donor positions j, j + 1, ..., n_dn - 1 for recipient r.
    dp = np.zeros((n_rc, n_dn + 1, k + 1))
    dp[:, :, 0] = 1.0

    for donor_pos in range(n_dn - 1, -1, -1):
        for kk in range(1, k + 1):
            dp[:, donor_pos, kk] = (
                dp[:, donor_pos + 1, kk]
                + row_weights[:, donor_pos] * dp[:, donor_pos + 1, kk - 1]
            )

    timing["setup"] += time.perf_counter() - t0

    # -----------------------------
    # Allocation
    # -----------------------------
    t0 = time.perf_counter()

    est = np.empty(n_mcmc)
    estdiff = np.empty(n_mcmc)
    var = np.empty(n_mcmc)
    varpaired = np.empty(n_mcmc)

    Y_rc = Y[rc]
    Y_matches = np.empty((n_rc, k))
    match_cols = np.empty((n_rc, k), dtype=int)
    g_oc = np.concatenate([np.ones(n_rc), np.zeros(k * n_rc)])
    match_weights = np.full(n_rc * k, 1 / k)

    timing["allocation"] += time.perf_counter() - t0

    # -----------------------------
    # Monte Carlo Sampling
    # -----------------------------
    for j in range(n_mcmc):

        # ===== Sampling =====
        t0 = time.perf_counter()

        for r in range(n_rc):
            remaining = k
            selected = 0

            # Scan donors left to right. Conditional on the current remaining
            # cardinality, include donor j with probability
            # r_ij * E[j + 1, remaining - 1] / E[j, remaining].
            for donor_pos in range(n_dn):
                if remaining == 0:
                    break

                denom = dp[r, donor_pos, remaining]
                numer = (
                    row_weights[r, donor_pos]
                    * dp[r, donor_pos + 1, remaining - 1]
                )
                include_prob = numer / denom

                if rng.random() < include_prob:
                    match_cols[r, selected] = donor_pos
                    selected += 1
                    remaining -= 1

            if remaining != 0:
                raise RuntimeError("Monte Carlo row sampler failed to select k donors.")

            Y_matches[r, :] = Y[dn[match_cols[r, :]]]

        timing["mcmc_sampling"] += time.perf_counter() - t0

        # ===== Estimation =====
        t0 = time.perf_counter()

        Y_match = Y_matches.ravel()
        Y_oc = np.concatenate([Y_rc, Y_match])

        est[j], var[j] = lm_slope_variance(Y_oc, g_oc)

        estdiff[j] = (
            Y_rc.mean()
            - np.average(
                Y_match,
                weights=match_weights
            )
        )

        pair_avg = Y_matches.mean(axis=1)
        varpaired[j] = (
            np.var(Y_rc - pair_avg, ddof=1)
            / len(Y_rc)
        )

        timing["mcmc_estimation"] += time.perf_counter() - t0

    # -----------------------------
    # Summary
    # -----------------------------
    t0 = time.perf_counter()

    est_bar, u_bar, b, t = mi_total_variance(est, var)
    _, u_paired, _, t_paired = mi_total_variance(est, varpaired)

    timing["summary"] += time.perf_counter() - t0

    result = {
        "est": est_bar,
        "est_med": float(np.median(est)),
        "k": k,
        "wiVar": u_bar,
        "bwVar": b,
        "miVar": t,
        "wiVarpaired": u_paired,
        "miVarpaired": t_paired,
        "estdiff": float(estdiff.mean()),
    }

    if profile:
        sampling_time = timing["mcmc_sampling"]
        estimation_time = timing["mcmc_estimation"]
        mcmc_time = sampling_time + estimation_time
        method_time = sum(timing.values())

        result.update({f"time_{k}": v for k, v in timing.items()})

        result["time_mcmc_total"] = mcmc_time
        result["time_method_total"] = method_time

        # Here a "sampling draw" means one recipient row sampled directly.
        result["n_sampling_draws"] = int(n_mcmc * n_rc)
        result["time_per_sampling_draw"] = (
            sampling_time / result["n_sampling_draws"]
        )

        result["share_sampling_in_mcmc"] = (
            sampling_time / mcmc_time
        )
        result["share_sampling_in_method"] = (
            sampling_time / method_time
        )
        result["share_estimation_in_method"] = (
            estimation_time / method_time
        )
        result["share_mcmc_in_method"] = (
            mcmc_time / method_time
        )

    return result


def run_constrained_1to1(X, Y, g, n_mcmc, beta, pstar, rng, profile=False):
    timing = new_timer()

    # -----------------------------
    # Setup
    # -----------------------------
    t0 = time.perf_counter()

    rc, dn = recipient_donor_indices(g)
    n_rc = len(rc)
    n_dn = len(dn)

    dist = distance_matrix(X, g, 2)
    C_init = init_constrained_greedy(dist, k=1)
    match_cols = np.argmax(C_init, axis=1)
    unmatched_donors = np.where(C_init.sum(axis=0) == 0)[0]

    timing["setup"] += time.perf_counter() - t0

    # -----------------------------
    # Allocation
    # -----------------------------
    t0 = time.perf_counter()

    est = np.empty(n_mcmc)
    estdiff = np.empty(n_mcmc)
    var = np.empty(n_mcmc)
    varclus = np.empty(n_mcmc)
    varpaired = np.empty(n_mcmc)
    tr = np.zeros(n_mcmc, dtype=bool)

    Y_rc = Y[rc]
    g_oc = np.concatenate([np.ones(n_rc), np.zeros(n_rc)])
    subclass = np.concatenate([np.arange(n_rc), np.arange(n_rc)])

    timing["allocation"] += time.perf_counter() - t0

    # -----------------------------
    # MCMC
    # -----------------------------
    for j in range(n_mcmc):

        # ===== Sampling =====
        t0 = time.perf_counter()

        flag = rng.choice([1, 2], p=[pstar, 1 - pstar])

        if flag == 1:
            # Swap the donors assigned to two recipients. This is the same
            # proposal as swapping two rows of C_acc, represented by indices.
            row1 = rng.integers(n_rc)
            row2 = rng.integers(n_rc - 1)
            if row2 >= row1:
                row2 += 1

            old1 = match_cols[row1]
            old2 = match_cols[row2]

            loglik_nr = -beta * (dist[row1, old2] + dist[row2, old1])
            loglik_dr = -beta * (dist[row1, old1] + dist[row2, old2])

        else:
            if len(unmatched_donors) == 0:
                loglik_nr = -np.inf
                loglik_dr = 0.0
            else:
                unmatched_pos = rng.integers(len(unmatched_donors))
                dn_new = unmatched_donors[unmatched_pos]
                rc_new = rng.integers(0, n_rc)

                old = match_cols[rc_new]

                loglik_nr = -beta * dist[rc_new, dn_new]
                loglik_dr = -beta * dist[rc_new, old]

        loglik_ratio = loglik_nr - loglik_dr
        logr = min(0.0, loglik_ratio)

        if np.log(rng.random()) < logr:
            if flag == 1:
                match_cols[row1] = old2
                match_cols[row2] = old1
            else:
                if len(unmatched_donors) > 0:
                    match_cols[rc_new] = dn_new
                    unmatched_donors[unmatched_pos] = old
            tr[j] = True

        timing["mcmc_sampling"] += time.perf_counter() - t0

        # ===== Estimation =====
        t0 = time.perf_counter()

        Y_match = Y[dn[match_cols]]

        Y_oc = np.concatenate([Y_rc, Y_match])

        est[j], var[j] = lm_slope_variance(Y_oc, g_oc)
        estdiff[j] = Y_rc.mean() - Y_match.mean()
        varclus[j] = cluster_se_for_slope(Y_oc, g_oc, subclass)
        varpaired[j] = np.var(Y_rc - Y_match, ddof=1) / len(Y_rc)

        timing["mcmc_estimation"] += time.perf_counter() - t0

    # -----------------------------
    # Summary
    # -----------------------------
    t0 = time.perf_counter()

    est_bar, u_bar, b, t = mi_total_variance(est, var)
    _, u_clus, _, t_clus = mi_total_variance(est, varclus)
    _, u_paired, _, t_paired = mi_total_variance(est, varpaired)

    timing["summary"] += time.perf_counter() - t0

    result = {
        "est": est_bar,
        "est_med": float(np.median(est)),
        "wiVar": u_bar,
        "wiVarclus": u_clus,
        "bwVar": b,
        "miVar": t,
        "miVarclus": t_clus,
        "wiVarpaired": u_paired,
        "miVarpaired": t_paired,
        "estdiff": float(estdiff.mean()),
        "acceptance_count": int(tr.sum()),
    }

    if profile:
        sampling_time = timing["mcmc_sampling"]
        estimation_time = timing["mcmc_estimation"]
        mcmc_time = sampling_time + estimation_time
        method_time = sum(timing.values())

        result.update({f"time_{k}": v for k, v in timing.items()})

        result["time_mcmc_total"] = mcmc_time
        result["time_method_total"] = method_time

        # Here a "sampling draw" means one full constrained proposal update.
        result["n_sampling_draws"] = int(n_mcmc)
        result["time_per_sampling_draw"] = sampling_time / result["n_sampling_draws"]

        result["share_sampling_in_mcmc"] = sampling_time / mcmc_time
        result["share_sampling_in_method"] = sampling_time / method_time
        result["share_estimation_in_method"] = estimation_time / method_time
        result["share_mcmc_in_method"] = mcmc_time / method_time

    return result


def run_constrained_1to1_gibbs(X, Y, g, n_mcmc, beta, pstar, rng, profile=False):
    timing = new_timer()

    # -----------------------------
    # Setup
    # -----------------------------
    t0 = time.perf_counter()

    rc, dn = recipient_donor_indices(g)
    n_rc = len(rc)
    n_dn = len(dn)

    dist = distance_matrix(X, g, 2)
    C_init = init_constrained_greedy(dist, k=1)
    match_cols = np.argmax(C_init, axis=1)
    unmatched_donors = np.where(C_init.sum(axis=0) == 0)[0]

    # Row-wise shifting keeps the full conditional probabilities unchanged
    # while avoiding unnecessary underflow for larger beta values.
    shifted_dist = dist - dist.min(axis=1, keepdims=True)
    base_weights = np.exp(-beta * shifted_dist)

    timing["setup"] += time.perf_counter() - t0

    # -----------------------------
    # Allocation
    # -----------------------------
    t0 = time.perf_counter()

    est = np.empty(n_mcmc)
    estdiff = np.empty(n_mcmc)
    var = np.empty(n_mcmc)
    varclus = np.empty(n_mcmc)
    varpaired = np.empty(n_mcmc)
    changed_iteration = np.zeros(n_mcmc, dtype=bool)

    Y_rc = Y[rc]
    g_oc = np.concatenate([np.ones(n_rc), np.zeros(n_rc)])
    subclass = np.concatenate([np.arange(n_rc), np.arange(n_rc)])

    n_unmatched = len(unmatched_donors)
    weights = np.empty(n_unmatched + 1)
    cdf = np.empty(n_unmatched + 1)
    change_count = 0

    timing["allocation"] += time.perf_counter() - t0

    # -----------------------------
    # MCMC
    # -----------------------------
    for j in range(n_mcmc):
        changed_any = False

        # ===== Sampling =====
        t0 = time.perf_counter()

        for r in range(n_rc):
            current = match_cols[r]

            # Slot 0 is the recipient's current donor. Slots 1: are donors
            # currently unmatched by all other recipients.
            weights[0] = base_weights[r, current]
            if n_unmatched:
                weights[1:] = base_weights[r, unmatched_donors]

            np.cumsum(weights, out=cdf)
            slot = np.searchsorted(cdf, rng.random() * cdf[-1], side="right")

            if slot > 0:
                unmatched_pos = slot - 1
                new = unmatched_donors[unmatched_pos]

                match_cols[r] = new
                unmatched_donors[unmatched_pos] = current
                changed_any = True
                change_count += 1

        changed_iteration[j] = changed_any
        timing["mcmc_sampling"] += time.perf_counter() - t0

        # ===== Estimation =====
        t0 = time.perf_counter()

        Y_match = Y[dn[match_cols]]
        Y_oc = np.concatenate([Y_rc, Y_match])

        est[j], var[j] = lm_slope_variance(Y_oc, g_oc)
        estdiff[j] = Y_rc.mean() - Y_match.mean()
        varclus[j] = cluster_se_for_slope(Y_oc, g_oc, subclass)
        varpaired[j] = np.var(Y_rc - Y_match, ddof=1) / len(Y_rc)

        timing["mcmc_estimation"] += time.perf_counter() - t0

    # -----------------------------
    # Summary
    # -----------------------------
    t0 = time.perf_counter()

    est_bar, u_bar, b, t = mi_total_variance(est, var)
    _, u_clus, _, t_clus = mi_total_variance(est, varclus)
    _, u_paired, _, t_paired = mi_total_variance(est, varpaired)

    timing["summary"] += time.perf_counter() - t0

    result = {
        "est": est_bar,
        "est_med": float(np.median(est)),
        "wiVar": u_bar,
        "wiVarclus": u_clus,
        "bwVar": b,
        "miVar": t,
        "miVarclus": t_clus,
        "wiVarpaired": u_paired,
        "miVarpaired": t_paired,
        "estdiff": float(estdiff.mean()),
        "change_count": int(change_count),
        "changed_iteration_count": int(changed_iteration.sum()),
    }

    if profile:
        sampling_time = timing["mcmc_sampling"]
        estimation_time = timing["mcmc_estimation"]
        mcmc_time = sampling_time + estimation_time
        method_time = sum(timing.values())

        result.update({f"time_{k}": v for k, v in timing.items()})

        result["time_mcmc_total"] = mcmc_time
        result["time_method_total"] = method_time

        # Here a "sampling draw" means one recipient Gibbs update.
        result["n_sampling_draws"] = int(n_mcmc * n_rc)
        result["time_per_sampling_draw"] = sampling_time / result["n_sampling_draws"]

        result["share_sampling_in_mcmc"] = sampling_time / mcmc_time
        result["share_sampling_in_method"] = sampling_time / method_time
        result["share_estimation_in_method"] = estimation_time / method_time
        result["share_mcmc_in_method"] = mcmc_time / method_time

    return result


def run_constrained_1tok(X, Y, g, n_mcmc, beta, pstar, rng, k=2, profile=False):
    timing = new_timer()

    # -----------------------------
    # Setup
    # -----------------------------
    t0 = time.perf_counter()

    rc, dn = recipient_donor_indices(g)
    n_rc = len(rc)
    n_dn = len(dn)

    if k < 1 or n_rc * k > n_dn:
        raise ValueError("constrained 1-to-k matching requires k >= 1 and n_rc * k <= n_dn.")

    dist = distance_matrix(X, g, 2)
    C_acc = init_constrained_greedy(dist, k=k)
    match_cols = np.array([np.where(C_acc[s] == 1)[0] for s in range(n_rc)])
    unmatched_donors = np.where(C_acc.sum(axis=0) == 0)[0]

    const = beta

    timing["setup"] += time.perf_counter() - t0

    # -----------------------------
    # Allocation
    # -----------------------------
    t0 = time.perf_counter()

    est = np.empty(n_mcmc)
    var = np.empty(n_mcmc)
    varclus = np.empty(n_mcmc)
    tr = np.zeros(n_mcmc, dtype=bool)

    Y_rc = Y[rc]
    Y_matches = np.empty((n_rc, k))
    g_oc = np.concatenate([np.ones(n_rc), np.zeros(k * n_rc)])
    weights = np.concatenate([np.ones(n_rc), np.full(k * n_rc, 1 / k)])
    subclass = np.concatenate([np.arange(n_rc), np.repeat(np.arange(n_rc), k)])

    timing["allocation"] += time.perf_counter() - t0

    # -----------------------------
    # MCMC
    # -----------------------------
    for j in range(n_mcmc):

        # ===== Sampling =====
        t0 = time.perf_counter()

        flag = rng.choice([1, 2], p=[pstar, 1 - pstar])

        if flag == 1:
            row1 = rng.integers(n_rc)
            row2 = rng.integers(n_rc - 1)
            if row2 >= row1:
                row2 += 1

            slot1 = rng.integers(k)
            slot2 = rng.integers(k)
            old1 = match_cols[row1, slot1]
            old2 = match_cols[row2, slot2]

            loglik_nr = -const * (dist[row1, old2] + dist[row2, old1])
            loglik_dr = -const * (dist[row1, old1] + dist[row2, old2])

        else:
            if len(unmatched_donors) == 0:
                loglik_nr = -np.inf
                loglik_dr = 0.0
            else:
                unmatched_pos = rng.integers(len(unmatched_donors))
                dn_new = unmatched_donors[unmatched_pos]
                rc_new = rng.integers(0, n_rc)
                slot = rng.integers(k)
                old = match_cols[rc_new, slot]

                loglik_nr = -const * dist[rc_new, dn_new]
                loglik_dr = -const * dist[rc_new, old]

        loglik_ratio = loglik_nr - loglik_dr
        logr = min(0.0, loglik_ratio)
        if np.log(rng.random()) < logr:
            if flag == 1:
                match_cols[row1, slot1] = old2
                match_cols[row2, slot2] = old1
            else:
                if len(unmatched_donors) > 0:
                    match_cols[rc_new, slot] = dn_new
                    unmatched_donors[unmatched_pos] = old
            tr[j] = True

        timing["mcmc_sampling"] += time.perf_counter() - t0

        # ===== Estimation =====
        t0 = time.perf_counter()

        index_match = match_cols

        Y_matches[:, :] = Y[dn[index_match]]
        Y_match = Y_matches.ravel()
        Y_oc = np.concatenate([Y_rc, Y_match])

        est[j], var[j] = wls_slope_variance(Y_oc, g_oc, weights)

        # R code calls unweighted lm inside coeftest even though reg is weighted.
        varclus[j] = cluster_se_for_slope(Y_oc, g_oc, subclass)

        timing["mcmc_estimation"] += time.perf_counter() - t0

    # -----------------------------
    # Summary
    # -----------------------------
    t0 = time.perf_counter()

    est_bar, u_bar, b, t = mi_total_variance(est, var)
    _, u_clus, _, t_clus = mi_total_variance(est, varclus)

    timing["summary"] += time.perf_counter() - t0

    result = {
        "est": est_bar,
        "est_med": float(np.median(est)),
        "wiVar": u_bar,
        "wiVarclus": u_clus,
        "bwVar": b,
        "miVar": t,
        "miVarclus": t_clus,
        "acceptance_count": int(tr.sum()),
    }

    if profile:
        sampling_time = timing["mcmc_sampling"]
        estimation_time = timing["mcmc_estimation"]
        mcmc_time = sampling_time + estimation_time
        method_time = sum(timing.values())

        result.update({f"time_{k}": v for k, v in timing.items()})

        result["time_mcmc_total"] = mcmc_time
        result["time_method_total"] = method_time

        # Here a "sampling draw" means one full constrained proposal update.
        result["n_sampling_draws"] = int(n_mcmc)
        result["time_per_sampling_draw"] = sampling_time / result["n_sampling_draws"]

        result["share_sampling_in_mcmc"] = sampling_time / mcmc_time
        result["share_sampling_in_method"] = sampling_time / method_time
        result["share_estimation_in_method"] = estimation_time / method_time
        result["share_mcmc_in_method"] = mcmc_time / method_time

    return result


