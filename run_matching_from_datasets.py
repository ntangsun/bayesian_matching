#!/usr/bin/env python3
"""
run_matching_from_datasets.py

Run the Bayesian matching simulations using datasets generated by
generate_datasets.py. This script does NOT recreate the datasets.

Examples:
    python generate_datasets.py --n-sim 100 --out datasets.npz

    python run_matching_from_datasets.py --datasets datasets.npz --method unconstrained_1to1 --n-mcmc 1000 --out unconstrained_1to1.csv
    python run_matching_from_datasets.py --datasets datasets.npz --method unconstrained_1tok --n-mcmc 1000 --out unconstrained_1tok.csv
    python run_matching_from_datasets.py --datasets datasets.npz --method constrained_1to1 --n-mcmc 1000 --out constrained_1to1.csv
    python run_matching_from_datasets.py --datasets datasets.npz --method constrained_1tok --n-mcmc 1000 --out constrained_1tok.csv

Notes:
- This is a close Python translation of the uploaded R logic, with the data-generation
  loop removed.
- The original pasted R code has undefined names `p`, `const`, and `Start.sim`.
  Here, `p = beta`, `const = beta`, and indexing starts at 1 conceptually.
- The R code initializes constrained matching using StatMatch/MatchIt. This Python
  translation uses deterministic greedy nearest-distance initialization so it can run
  without R packages. The MCMC target/update logic follows the uploaded code.
"""

