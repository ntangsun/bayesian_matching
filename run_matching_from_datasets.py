def next_incremented_path(out_dir, prefix="results", suffix=".csv"):
    """Return results_1.csv, results_2.csv, ... inside out_dir."""
    out_dir = Path(out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    pattern = re.compile(rf"^{re.escape(prefix)}_(\d+){re.escape(suffix)}$")
    used = []
    for path in out_dir.glob(f"{prefix}_*{suffix}"):
        match = pattern.match(path.name)
        if match:
            used.append(int(match.group(1)))

    next_id = max(used, default=0) + 1
    return out_dir / f"{prefix}_{next_id}{suffix}"


def add_ci(result, true_tau=1.0, var_col="miVar"):
    """Add normal-approximation CI and coverage indicator to one simulation result."""
    if var_col in result and np.isfinite(result[var_col]) and result[var_col] >= 0:
        se = float(np.sqrt(result[var_col]))
        result["ll"] = float(result["est"] - 1.96 * se)
        result["ul"] = float(result["est"] + 1.96 * se)
        result["covers_true"] = int(result["ll"] <= true_tau <= result["ul"])
    return result


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--datasets", type=str, required=True)
    parser.add_argument("--method", type=str, required=True, choices=METHODS.keys())
    parser.add_argument("--n-mcmc", type=int, default=100000)
    parser.add_argument("--beta", type=float, default=1.0)
    parser.add_argument("--k", type=int, default=2, help="Number of donors per recipient for 1-to-k methods")
    parser.add_argument("--pstar", type=float, default=0.5)
    parser.add_argument("--max-sim", type=int, default=None, help="Run only first max-sim datasets")
    parser.add_argument("--true-tau", type=float, default=1.0)
    parser.add_argument("--profile", action="store_true", help="Record runtime breakdown columns")

    parser.add_argument(
        "--out-dir",
        type=str,
        default=None,
        help=(
            "Folder where result CSVs are saved. If omitted, or set to the "
            "legacy value 'results', uses results/matching/<method>. Other "
            "custom directories are used exactly as provided."
        ),
    )
    parser.add_argument("--out-prefix", type=str, default="results", help="Prefix for auto-numbered CSVs")
    parser.add_argument("--out", type=str, default=None, help="Exact output path. If omitted, saves out-dir/out-prefix_#.csv")
    parser.add_argument("--print-every", type=int, default=10, help="Print progress every this many simulations")
    args = parser.parse_args()

    if args.out is None:
        requested_out_dir = Path(args.out_dir) if args.out_dir is not None else None
        if requested_out_dir is None or requested_out_dir == Path("results"):
            out_dir = Path("results") / "matching" / args.method
        else:
            out_dir = requested_out_dir
        out_path = next_incremented_path(out_dir, prefix=args.out_prefix, suffix=".csv")
    else:
        out_path = Path(args.out)
        out_path.parent.mkdir(parents=True, exist_ok=True)

    data = np.load(args.datasets)
    X_all = data["X"]
    Y_all = data["Y"]
    g_all = data["g"]
    seeds = data["seeds"] if "seeds" in data else np.arange(1, len(X_all) + 1)

    n_sim = len(X_all)
    if args.max_sim is not None:
        n_sim = min(n_sim, args.max_sim)

    rows = []
    method_func = METHODS[args.method]

    for s in range(n_sim):
        if s == 0 or (s + 1) % args.print_every == 0 or (s + 1) == n_sim:
            print(f"Simulation {s + 1}/{n_sim}")

        sim_start = time.perf_counter()

        prep_start = time.perf_counter()
        X = X_all[s]
        Y = Y_all[s]
        g = g_all[s]
        rng = np.random.default_rng(int(seeds[s]) + 1_000_000)
        sim_prep_time = time.perf_counter() - prep_start

        method_start = time.perf_counter()
        if args.method in {"constrained_1to1", "constrained_1to1_gibbs", "constrained_1tok"}:
            result = method_func(
                X, Y, g,
                args.n_mcmc,
                args.beta,
                args.pstar,
                rng,
                profile=args.profile,
            )
        elif args.method in {"unconstrained_1tok", "unconstrained_1tok_mc"}:
            result = method_func(
                X, Y, g,
                args.n_mcmc,
                args.beta,
                rng,
                k=args.k,
                profile=args.profile,
            )
        else:
            result = method_func(
                X, Y, g,
                args.n_mcmc,
                args.beta,
                rng,
                profile=args.profile,
            )
        method_time = time.perf_counter() - method_start

        post_start = time.perf_counter()
        result = add_ci(result, true_tau=args.true_tau, var_col="miVar")
        result = {
            "sim": s + 1,
            "method": args.method,
            "beta": args.beta,
            "pstar": args.pstar,
            "n_mcmc": args.n_mcmc,
            "dataset_file": str(args.datasets),
            "result_file": str(out_path),
            **result,
        }
        sim_post_time = time.perf_counter() - post_start

        sim_total_time = time.perf_counter() - sim_start

        if args.profile:
            result["time_sim_prep"] = sim_prep_time
            result["time_method_call"] = method_time
            result["time_sim_post"] = sim_post_time
            result["time_sim_total"] = sim_total_time
            result["share_method_in_sim"] = method_time / sim_total_time

        rows.append(result)

    fieldnames = sorted(set().union(*(r.keys() for r in rows)))
    with open(out_path, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    print(f"Saved results to {out_path}")

if __name__ == "__main__":
    main()
