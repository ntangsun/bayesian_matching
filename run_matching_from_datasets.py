def init_unconstrained_k_nearest(dist, k):
    """
    Replacement allowed across recipients.
    Returns C with k ones per row.
    """
    n_rc, n_dn = dist.shape
    C = np.zeros((n_rc, n_dn), dtype=int)
    nearest = np.argsort(dist, axis=1)[:, :k]
    for i in range(n_rc):
        C[i, nearest[i]] = 1
    return C


def init_constrained_greedy(dist, k):
    """
    Simple feasible greedy initialization without replacement.
    This replaces R's StatMatch/MatchIt initialization.
    """
    n_rc, n_dn = dist.shape
    C = np.zeros((n_rc, n_dn), dtype=int)
    used = set()

    for _ in range(k):
        for i in range(n_rc):
            order = np.argsort(dist[i])
            for j in order:
                if int(j) not in used:
                    C[i, j] = 1
                    used.add(int(j))
                    break

    return C


